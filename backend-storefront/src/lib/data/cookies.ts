/**
 * Guest order-confirmation token. After a guest places an order, the backend
 * (POST /store/orders/confirmation-token) mints a signed token authorizing
 * reads of that one order; we stash it httpOnly per order id so the
 * (IDOR-hardened) GET /store/orders/:id will serve the confirmation page even
 * without a customer session. Scoped to the order id, modest TTL.
 */
const orderTokenCookie = (orderId: string) => `_order_token_${orderId}`

export const setOrderToken = async (orderId: string, token: string) => {
  const cookies = await nextCookies()
  cookies.set(orderTokenCookie(orderId), token, {
    maxAge: 60 * 60 * 24 * 7,
    httpOnly: true,
    sameSite: "strict",
    secure: process.env.NODE_ENV === "production",
  })
}

export const getOrderToken = async (
  orderId: string
): Promise<string | undefined> => {
  try {
    const cookies = await nextCookies()
    return cookies.get(orderTokenCookie(orderId))?.value
  } catch {
    return undefined
  }
}
